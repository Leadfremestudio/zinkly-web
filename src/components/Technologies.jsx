export default function Technologies() {
  const sectionRef = useRef(null);

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.querySelectorAll('.tech-pill').forEach((pill, i) => {
              pill.style.transitionDelay = `${i * 60}ms`;
              pill.classList.add('tech-pill--visible');
            });
            entry.target.querySelector('.tech-section-header')?.classList.add('tech-header--visible');
          }
        });
      },
      { threshold: 0.1 }
    );
    if (sectionRef.current) observer.observe(sectionRef.current);
    return () => observer.disconnect();
  }, []);

  return (
    <section className="tech-section" ref={sectionRef} id="technologies">
      {/* Section header */}
      <div className="tech-section-header">
        <span className="tech-badge">TECHNOLOGIES</span>
        <h2 className="tech-heading">
          Speed Up your Work Flow With Our<br />Technologies
        </h2>
      </div>

      {/* Pills grid */}
      <div className="tech-grid">
        {technologies.map(({ name, Icon, bg }) => (
          <div className="tech-pill" key={name}>
            <span className="tech-icon-wrap" style={{ background: bg }}>
              <Icon />
            </span>
            <span className="tech-name">{name}</span>
          </div>
        ))}
      </div>
    </section>
  );
}
